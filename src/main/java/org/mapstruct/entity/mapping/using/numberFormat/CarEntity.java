package org.mapstruct.entity.mapping.using.numberFormat;

public class CarEntity {
	private int id;
	private double price;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public double getPrice() {
		return price;
	}

	public void setPrice(double price) {
		this.price = price;
	}
}